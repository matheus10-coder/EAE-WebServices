package net.abcbs.eae.pojo;

public class NurseLicensureResponse {
	private String state;
	private String userName;
	private String licenseType;
	private String licenseStart;
	private String licenseEnd;
	private String stateIssued;
	private String compactState;
	private String status;
	private String isActive;
	private String county;
	private String affiniteStatus;
	
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getLicenseType() {
		return licenseType;
	}
	public void setLicenseType(String licenseType) {
		this.licenseType = licenseType;
	}
	public String getLicenseStart() {
		return licenseStart;
	}
	public void setLicenseStart(String licenseStart) {
		this.licenseStart = licenseStart;
	}
	public String getLicenseEnd() {
		return licenseEnd;
	}
	public void setLicenseEnd(String licenseEnd) {
		this.licenseEnd = licenseEnd;
	}
	public String getStateIssued() {
		return stateIssued;
	}
	public void setStateIssued(String stateIssued) {
		this.stateIssued = stateIssued;
	}
	public String getCompactState() {
		return compactState;
	}
	public void setCompactState(String compactState) {
		this.compactState = compactState;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getIsActive() {
		return isActive;
	}
	public void setIsActive(String isActive) {
		this.isActive = isActive;
	}
	public String getCounty() {
		return county;
	}
	public void setCounty(String county) {
		this.county = county;
	}
	public String getAffiniteStatus() {
		return affiniteStatus;
	}
	public void setAffiniteStatus(String affiniteStatus) {
		this.affiniteStatus = affiniteStatus;
	}

}
